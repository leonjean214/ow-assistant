export function getLang() {
  try {
    return window.localStorage.getItem(LANG_KEY) === "en" ? "en" : "zh";
  } catch {
    return "zh";
  }
}

export function setLang(lang) {
  const next = lang === "en" ? "en" : "zh";
  try {
    window.localStorage.setItem(LANG_KEY, next);
  } catch {
    // Language persistence is optional if storage is unavailable.
  }
  applyLang();
  return next;
}

export function t(key, vars = {}) {
  const lang = getLang();
  const table = STRINGS[lang] || STRINGS.zh;
  const template = table[key] ?? STRINGS.zh[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => (
    Object.prototype.hasOwnProperty.call(vars, name) ? String(vars[name]) : `{${name}}`
  ));
}

export function roleLabel(role) {
  if (role === "tank") return t("roleTank");
  if (role === "damage") return t("roleDamage");
  if (role === "support") return t("roleSupport");
  return role || "";
}

export function applyLang(options = {}) {
  const lang = getLang();
  document.documentElement.lang = lang === "en" ? "en" : "zh-CN";
  document.title = t("appTitle");
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    node.setAttribute("aria-label", t(node.dataset.i18nAriaLabel));
  });
  document.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.setAttribute("title", t(node.dataset.i18nTitle));
  });
  if (options.emit !== false) {
    window.dispatchEvent(new CustomEvent("ow:langchange", { detail: { lang } }));
  }
}
